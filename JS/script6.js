// 3. Скрипт, який поверне суму всіх чисел в масиві
let numbers2 = [5, 10, 15, 20];
let sum = 0;
for (let i = 0; i < numbers2.length; i++) {
  sum += numbers2[i];
}
console.log("Сума чисел у масиві:", sum);

// 4. Створити масив з 5-ти чисел. Вивести на екран всі елементи масиву за допомогою циклу for
let numbers3 = [2, 4, 6, 8, 10];
for (let i = 0; i < numbers3.length; i++) {
  console.log("Елемент масиву:", numbers3[i]);
}

// 5. Створити масив із 5-ти рядків. Вивести на екран кожен рядок, який містить більше 5-ти символів
let words = ["Кіт", "Сонце", "Програмування", "Книга", "Дружба"];
for (let i = 0; i < words.length; i++) {
  if (words[i].length > 5) {
    console.log("Рядок більше 5 символів:", words[i]);
  }
}

// 6. Створити масив з 10-ти чисел. Знайти та вивести максимальне значення з масиву
let numbers4 = [12, 45, 7, 89, 23, 56, 3, 99, 32, 10];
let max = numbers4[0];
for (let i = 1; i < numbers4.length; i++) {
  if (numbers4[i] > max) {
    max = numbers4[i];
  }
}
console.log("Максимальне число в масиві:", max);

